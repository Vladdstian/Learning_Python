def add_numbers(n1, n2):
    """Adds two numbers together"""
    return n1 + n2


def subst_numbers(n1, n2):
    """Subtracts a number from another"""
    return n1 - n2


def multiply_numbers(n1, n2):
    """Multiplies two numbers together"""
    return n1 * n2


def divide_numbers(n1, n2):
    """Divides two numbers"""
    return n1 / n2


def divide_rest(n1, n2):
    """Gives the rest of division between two numbers"""
    return n1 % n2
